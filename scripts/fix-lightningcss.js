/**
 * Postinstall fix for EAS Update / Expo Go compatibility:
 *
 * 1. react-native-css-interop embeds its own copy of lightningcss which tries
 *    to load a platform-specific .node binary that may not be present on EAS
 *    Build macOS workers. This script patches the embedded lightningcss to
 *    redirect to the root-level lightningcss (which has proper optional deps).
 *
 * 2. react-native-css-interop generates .cache files (web.css, android.js, etc.)
 *    during npm install. Metro tries to bundle these files and fails because they
 *    are not proper JS modules. This script deletes the .cache directory after
 *    every npm install to prevent Metro from picking them up.
 */
const fs = require('fs');
const path = require('path');

// ── Fix 1: Patch embedded lightningcss ──────────────────────────────────────

const targetFile = path.join(
  __dirname,
  '..',
  'node_modules',
  'react-native-css-interop',
  'node_modules',
  'lightningcss',
  'node',
  'index.js'
);

if (!fs.existsSync(targetFile)) {
  console.log('[fix-lightningcss] lightningcss target not found, skipping patch.');
} else {
  const current = fs.readFileSync(targetFile, 'utf8');

  if (current.includes('// Patched by fix-lightningcss.js')) {
    console.log('[fix-lightningcss] lightningcss already patched, skipping.');
  } else {
    const patched = `// Patched by fix-lightningcss.js — redirect to root lightningcss for EAS Build compatibility
try {
  const root = require('lightningcss');
  module.exports = root;
  module.exports.browserslistToTargets = require('./browserslistToTargets');
  module.exports.composeVisitors = require('./composeVisitors');
  module.exports.Features = require('./flags').Features;
} catch (e) {
  // Fallback to original behavior
  let parts = [process.platform, process.arch];
  if (process.platform === 'linux') {
    const { MUSL, family } = require('detect-libc');
    if (family === MUSL) {
      parts.push('musl');
    } else if (process.arch === 'arm') {
      parts.push('gnueabihf');
    } else {
      parts.push('gnu');
    }
  } else if (process.platform === 'win32') {
    parts.push('msvc');
  }
  if (process.env.CSS_TRANSFORMER_WASM) {
    module.exports = require('../pkg');
  } else {
    try {
      module.exports = require('lightningcss-' + parts.join('-'));
    } catch (err) {
      module.exports = require('../lightningcss.' + parts.join('-') + '.node');
    }
  }
  module.exports.browserslistToTargets = require('./browserslistToTargets');
  module.exports.composeVisitors = require('./composeVisitors');
  module.exports.Features = require('./flags').Features;
}
`;
    fs.writeFileSync(targetFile, patched, 'utf8');
    console.log('[fix-lightningcss] lightningcss patch applied successfully.');
  }
}

// ── Fix 2: Delete .cache directories generated by react-native-css-interop ──
// Metro fails to bundle these files (web.css, android.js, etc.) because they
// are not valid JS modules. Deleting them after npm install prevents the error:
// "Failed to get the SHA-1 for: .../react-native-css-interop/.cache/android.js"

function deleteCacheDir(dir) {
  if (fs.existsSync(dir)) {
    fs.rmSync(dir, { recursive: true, force: true });
    console.log('[fix-lightningcss] Deleted .cache dir:', dir);
  }
}

// Delete .cache in all copies of react-native-css-interop (root + nested in nativewind)
const nodeModules = path.join(__dirname, '..', 'node_modules');
const cacheDirs = [
  path.join(nodeModules, 'react-native-css-interop', '.cache'),
  path.join(nodeModules, 'nativewind', 'node_modules', 'react-native-css-interop', '.cache'),
];

cacheDirs.forEach(deleteCacheDir);
console.log('[fix-lightningcss] Done.');
